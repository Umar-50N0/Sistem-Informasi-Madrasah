            <div class="tab-pane fade active in" id="sejarah">
                <h4>DTA Nurul Anwar</h4>
                Cikal bakal DTA Nurul Anwar adalah kegiatan mengaji setiap sore selepas shalat maghrib yang diadakan di sebuah Musholla bernama Nurul Anwar yang berada di desa Marikangen blok Kragilan tepatnya di RT 04/ RW 01 asuhan  Bpk. Kyai Anwar. 
Kegiatan mengaji tersebut semakin hari semakin meningkat jumlah pesertanya. Karena tempat yang tidak mencukupi, kegiatan mengaji tersebut di adakan di dua tempat, satu di Musholla Nurul Anwar dan satu lagi di Musholla yang tidak begitu jauh dari Musholla Nurul Anwar.
Melihat perkembangan dan antusiasme peserta yang baik, muncul ide untuk membangun sebuah tempat untuk mengaji. Kyai Anwar, Ust. Syatori, Bpk Sukarta, Bpk Samhudi dan beberapa warga setempat mengadakan diskusi untuk membahas pembuatan sekolah mengaji.
Diskusi tersebut membuahkan hasil yang baik. Diatas tanah yang diwakafkan oleh Bpk. Samhudi seluas 8x4 m2 didirikanlah Madrasah Ibtidaiyah Nurul Anwar pada tahun 1983. Bahan bangunan yang digunakan berasal dari sumbangan beberapa warga setempat. Santri yang mengajipun ikut membantu mengumpulkan batu dan pasir yang diambil dari sungai Soka yang mengalir di desa Marikangen.
Pimpinan Madrasah waktu itu adalah Bpk Kuzaeni memimpin dari awal berdiri sampai tahun 1990. Kemudian pimpinan diserahkan kepada Bpk. Nuruddin pada tahun 1990 – 1995. Tahun 1995 – 2000 pimpinan diserahkan kepada Bpk. Faqi. Kemudian dari tahun 2000 – sekarang pimpinan di pegang oleh Ust. Syatori.
Di masa-masa awal berdirinya madrasah, terjadi penurunan jumlah santri yang mengaji. Untuk mengatasi penurunan jumlah peserta didik tersebut, pada tahun 2002 dibuka sekolah TKQ (Taman Kanak-Kanak Quran) dan TPQ (Taman Pendidikan Quran) untuk menampung santri usia dini. Dan nama madrasah berubah menjadi TKQ-TPQ Madrasah Diniyah Amaliyah Nurul Anwar yang dikepalai oleh Ust. Syatori. 
Tahun 2009 terjadi perubahan keorganisasian dan pemecahan kepemimpinan. Dan nama madrasah mengalami perubahan mengikuti aturan pemerintah menjadi TKQ-TPQ Diniyah Takmiliyah Amaliyah (DTA) Nurul Anwar. TPQ dikepalai oleh Ust. Abdullah, S.Sos. Kemudian TKQ dikepalai oleh Ust. Soma, S.Pdi, sementara DTA dikepalai oleh Ust. Syatori.

